




// Reverse a string "paul is dead" using for loop.
// Declare const string
const string = ("paul is dead")
// Declare empty string
let newString = ""
// Start the loop at the end of the string. Length - 1 = index 11 ("d").
for (let i = string.length - 1; i >= 0; i--) {
    newString += string[i]
}
console.log(newString) 



